const formForProduct = document.forms.formForProduct;
const name = formForProduct.nameProduct;
const quantity = formForProduct.quantityProduct;
const price = formForProduct.priceProduct;
const image = formForProduct.imageProduct;
const submit = formForProduct.submit;

let productList = [];

formForProduct.addEventListener('submit', (ev) => {
    ev.preventDefault();
    let product = {
        name: name.value,
        quantity: quantity.value,
        price: price.value,
        image: image.value
    };
    productList.push(product);
    localStorage.setItem('products', JSON.stringify(productList));
});


